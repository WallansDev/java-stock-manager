package src;

public class Client {
    public String id;
    public String name;
    public String email;
    public String phone;
    public String address;

    public Client(String id, String name, String email, String phone, String address) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.address = address;
    }
}